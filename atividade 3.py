salario_atual = float(input("Digite o valor do salário atual: 1.400"))
percentual_aumento = float(input("Digite o percentual de aumento (em decimal): 10%"))

percentual_aumento = percentual_aumento / 100

salario_atualizado = salario_atual * (1 + percentual_aumento)

print("O novo salário é:", salario_atualizado)



